# Напишіть програму, яка запитує користувача ввести числа
# доти, доки він не введе число 0
# (коли вводиться число 0, цикл зупиняється),
# і виводить найбільше число із всіх введених чисел.
a = int(input('Введи числа:'))
max1 = 0
while a != 0:
    max1 = max(max1, a)
    a = int(input('Введи числа:'))
print(max1)